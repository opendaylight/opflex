package org.opendaylight.opflex.modb;

public class ConstInfo {

    private final String name;
    private final int value;

    public ConstInfo(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }
}
